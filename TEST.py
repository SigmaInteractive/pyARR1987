def rain_calculation(zone, index, year):
    with open("data.txt", "r+") as file: #r+ means reset and open
        file.write("time in seconds: | Rain mm/sec: \n") #Write column names to file
        print("time in seconds: | Rain mm/sec:")#Printing column names to terminal
        for i in range(1, len(zone[index])): #Looping for index 1 to 23
            time_in_sec = zone[index][0] * 60 #Time step calculation
            rain_mm_per_sec = (zone[index][i] * year[index]) / (zone[index][0] * 60) #Rain mm per sec calculation
            file.write(str(time_in_sec) + '         | ' + str(rain_mm_per_sec) + '\n') #Writing both calculations to file
            print(str(time_in_sec) + ' | ' + str(rain_mm_per_sec)) #Printing both calculations to terminal
        file.close()#Close file

